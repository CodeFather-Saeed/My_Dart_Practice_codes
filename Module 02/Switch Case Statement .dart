void main () {
  var marks = 45;

  switch (marks) {
    case 80:
      print("Result is A+ Grade");
      break;

    case 70:
      print("Result is A Grade");
      break;

    case 60:
      print("Result is A- Grade");
      break;

    case 50:
      print("Result is B Grade");
      break;

    case 40:
      print("Result is C Grade");
      break;

    default:
      print("Result is not found");
      break;
  }
}